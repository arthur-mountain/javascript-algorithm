/**
 * @param {number} n
 * @param {number[][]} edges
 * @return {number}
 */
var countCompleteComponents = function (n, edges) {
  /**
   * ====================================================================
   * 【 核心演算法心法與複雜度分析 】
   * ====================================================================
   * 1. 為什麼不能只用一次迴圈（邊建圖邊計算）？
   * - 「連通」的不變量具備單向合併性（一次遍歷可行）。
   * - 「完全（封閉）」的不變量要求每個點的鄰居數必須為 k - 1。新加入的邊會產生「後效性」，
   * 回頭改變或重組舊子圖的完全性，因此在變動狀態下無法即時結算，必須「先建圖、後處理」。
   *
   * * 2. 為什麼看似兩層嵌套迴圈，時間複雜度依然是完美的 O(V + E)？
   * - 透過 `visited` 陣列守門，每個節點一輩子只會面臨「二選一」的命運：
   * 【命運 A】作為子圖領頭羊，外層遇到未訪問，觸發 DFS 撈出整群，並由內層完整檢查一遍。
   * 【命運 B】作為子圖跟隨者，在前面的 DFS 內就被標記並檢查完，外層再遇到時直接跳過。
   * - 實質上每個節點與邊只會被處理一次，故總時間為 $O(V + E)$。
   */

  // ====================================================================
  // 1. 建立鄰接表 (Adjacency List)
  // ====================================================================
  // 確保包含所有 n 個頂點，孤立點（無鄰居）也會被正確初始化為空陣列。
  const graph = Array.from({ length: n }, () => []);
  for (const [u, v] of edges) {
    graph[u].push(v);
    graph[v].push(u);
  }

  // ====================================================================
  // 2. 初始化計數與走訪標記
  // ====================================================================
  let count = 0;
  const visited = Array.from({ length: n }, () => false);

  // ====================================================================
  // 3. 定義 DFS 走訪函式
  // ====================================================================
  // 用於識別獨立的子圖（連通元件），並收集當前元件內的所有頂點。
  const dfs = (node, componentVertices) => {
    visited[node] = true;
    componentVertices.push(node);

    // 遍歷鄰居節點
    for (const neighbor of graph[node]) {
      if (!visited[neighbor]) {
        dfs(neighbor, componentVertices);
      }
    }
  };

  // ====================================================================
  // 4. 主流程：遍歷所有頂點進行分群與完全性驗證
  // ====================================================================
  for (let i = 0; i < n; i++) {
    // 如果頂點未被走訪，代表發現了一個全新的子圖
    if (!visited[i]) {
      const componentVertices = [];

      // 透過 DFS 把跟頂點 i 連通的所有頂點收集進 componentVertices
      dfs(i, componentVertices);

      const k = componentVertices.length; // 當前子圖的總頂點數
      let isComplete = true;

      // 檢查此元件內的每一個點是否符合「完全圖」的定義：
      // 每個點的最終鄰居數（度數）都必須剛好是 k - 1。
      // 註：孤立點 (k = 1) 的鄰居數為 0 (1 - 1 = 0)，自然符合完全圖條件。
      for (const vertex of componentVertices) {
        if (graph[vertex].length !== k - 1) {
          isComplete = false; // 只要有一個點鄰居數不達標，此子圖就不完全
          break;
        }
      }

      // 只有完全符合完全圖條件的獨立子圖，計數才加 1
      if (isComplete) {
        count++;
      }
    }
  }

  // 5. 回傳符合條件的完全連通元件總數
  return count;
};
